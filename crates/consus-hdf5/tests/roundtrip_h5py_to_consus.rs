/// h5py writes a contiguous uint8 array (5,) → consus reads and verifies.
///
/// ## Invariant
///
/// Contiguous uint8 datasets from h5py are correctly read by consus.
#[test]
fn h5py_contiguous_u8_readable_by_consus() {
    let Some(bytes) = generate_fixture("contiguous_u8") else {
        return;
    };
    let hf = open_file(bytes);

    let addr = hf.open_path("/contiguous_u8").expect("open path");
    let ds = hf.dataset_at(addr).expect("dataset_at");

    // uint8 byte-order is not applicable (single byte); skip dtype assertion.
    assert_eq!(ds.shape.current_dims().as_slice(), &[5usize], "shape mismatch");

    let data_addr = ds.data_address.expect("data_address");
    let mut buf = vec![0u8; 5];
    hf.read_contiguous_dataset_bytes(data_addr, 0, &mut buf)
        .expect("read contiguous");
    assert_eq!(buf, vec![10u8, 20, 30, 40, 50], "values mismatch");
}

