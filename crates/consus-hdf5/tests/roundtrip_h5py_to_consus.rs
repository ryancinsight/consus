//! Roundtrip tests: h5py writer → consus reader.
//!
//! ## Purpose
//!
//! These tests verify that consus can correctly read HDF5 files produced by
//! h5py.  Each test:
//!
//! 1. Invokes `data/gen_h5py_compressed.py --case <name> --file <tmp>` to
//!    produce a fixture file using h5py.
//! 2. Opens the file with consus.
//! 3. Asserts the expected dataset structure and values.
//!
//! All tests skip gracefully when Python or h5py is unavailable.
//!
//! ## Coverage
//!
//! | Case                  | Dtype   | Shape     | Compression | Chunks |
//! |-----------------------|---------|-----------|-------------|--------|
//! | `deflate_1d_i32`      | int32   | (8,)      | deflate/6   | (4,)   |
//! | `deflate_2d_f64`      | float64 | (4,6)     | deflate/6   | (2,3)  |
//! | `deflate_3d_i32`      | int32   | (2,3,4)   | deflate/6   | (2,3,4)|
//! | `contiguous_u8`       | uint8   | (5,)      | none        | -      |
//! | `contiguous_f32`      | float32 | (4,)      | none        | -      |
//! | `contiguous_i16`      | int16   | (4,)      | none        | -      |
//! | `chunked_1d_f32_deflate` | float32 | (8,)   | deflate/6   | (4,)   |

use core::num::NonZeroUsize;
use std::path::PathBuf;
use std::process::Command;

use consus_core::{ByteOrder as CoreByteOrder, Datatype};
use consus_hdf5::file::Hdf5File;
use consus_io::MemCursor;

// ---------------------------------------------------------------------------
// Infrastructure helpers (mirrors roundtrip_consus_to_h5py.rs)
// ---------------------------------------------------------------------------

fn gen_script() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .unwrap()
        .parent()
        .unwrap()
        .join("data")
        .join("gen_h5py_compressed.py")
}

fn find_python() -> Option<String> {
    for candidate in &[r"D:\miniforge3\python.exe", "python3", "python"] {
        let ok = Command::new(candidate)
            .args(["-c", "import h5py"])
            .output()
            .map(|o| o.status.success())
            .unwrap_or(false);
        if ok {
            return Some((*candidate).to_string());
        }
    }
    None
}

/// Generate an HDF5 fixture file via h5py and return its bytes.
///
/// Returns `None` when Python/h5py is unavailable or the generator fails.
fn generate_fixture(case: &str) -> Option<Vec<u8>> {
    let script = gen_script();
    if !script.exists() {
        eprintln!("Skipping {case}: gen script not found at {script:?}");
        return None;
    }
    let python = find_python()?;

    let tmp = tempfile::NamedTempFile::new().ok()?;
    let tmp_path = tmp.path().to_owned();

    let output = Command::new(&python)
        .args([
            script.to_str().unwrap(),
            "--case",
            case,
            "--file",
            tmp_path.to_str().unwrap(),
        ])
        .output()
        .ok()?;

    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        eprintln!("Skipping {case}: gen script failed: {stderr}");
        return None;
    }

    // Read the generated bytes from the path (avoids cursor-position issues).
    std::fs::read(&tmp_path).ok()
}

fn open_file(bytes: Vec<u8>) -> Hdf5File<MemCursor> {
    let cursor = MemCursor::from_bytes(bytes);
    Hdf5File::open(cursor).expect("open HDF5 file")
}

// ---------------------------------------------------------------------------
// Datatype helpers
// ---------------------------------------------------------------------------

fn i32_le() -> Datatype {
    Datatype::Integer {
        bits: NonZeroUsize::new(32).unwrap(),
        byte_order: CoreByteOrder::LittleEndian,
        signed: true,
    }
}

fn f64_le() -> Datatype {
    Datatype::Float {
        bits: NonZeroUsize::new(64).unwrap(),
        byte_order: CoreByteOrder::LittleEndian,
    }
}


fn f32_le() -> Datatype {
    Datatype::Float {
        bits: NonZeroUsize::new(32).unwrap(),
        byte_order: CoreByteOrder::LittleEndian,
    }
}

fn i16_le() -> Datatype {
    Datatype::Integer {
        bits: NonZeroUsize::new(16).unwrap(),
        byte_order: CoreByteOrder::LittleEndian,
        signed: true,
    }
}

