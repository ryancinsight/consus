/// h5py writes deflate-compressed int32 array (8,) → consus reads and
/// verifies shape, dtype, and values 0..7.
///
/// ## Invariant
///
/// consus correctly decompresses and reads zlib-format deflate data
/// produced by h5py (filter ID 1).
#[test]
fn h5py_deflate_1d_i32_readable_by_consus() {
    let Some(bytes) = generate_fixture("deflate_1d_i32") else {
        return;
    };
    let hf = open_file(bytes);

    let addr = hf.open_path("/deflate_1d_i32").expect("open path");
    let ds = hf.dataset_at(addr).expect("dataset_at");

    assert_eq!(ds.datatype, i32_le(), "dtype mismatch");
    assert_eq!(ds.shape.current_dims().as_slice(), &[8usize], "shape mismatch");

    let raw = hf.read_chunked_dataset_all_bytes(addr).expect("read chunked");
    let values: Vec<i32> = raw
        .chunks_exact(4)
        .map(|b| i32::from_le_bytes(b.try_into().unwrap()))
        .collect();
    let expected: Vec<i32> = (0..8).collect();
    assert_eq!(values, expected, "values mismatch");
}

